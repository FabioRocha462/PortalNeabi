          <li>
            Sensibilizar e reunir pesquisadores, professores, técnico-administrativos, estudantes, representantes de entidades afins e demais interessados na temática das relações étnico-raciais;</li>
          <li>
            Colaborar e promover, por meio de patrcerias, ações estratégicas no âmbito da formação incial econtinuada dos profissionais do Sistema de Educação do Rio Grande do Norte; </li>
          <li>
            Contribuir para a ampliação do debate e da abrangência das políticas de ações afirmativas e depromoção de igualdade racial;</li>
          <li>
            Conduzir e divulgar conhecimentos sobre relações étnico-raciais junto às instituições educacionais, sociedade civil organizada e população em geral.</li>
