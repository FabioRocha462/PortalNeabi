          Ensino, Pesquisa e Extensão, que promovam o cumprimento efetivo das Leis 10.639/2003 e a 11.645/2008 e, os
          demais instrumentos legais correlatos.
        </p>
        <p>
           O NEABI é uma iniciativa política, pedagógica 
          institucional com o intuito de valorizar as diferentes
          manifestações culturais de etnias e segmentos sociais, preservando o direito à diversidade.
        </p>
        <p>
           As ações do NEABI estão direcionadas para a comunidade interna (alunos/as da Educação Básica,
          profissional, e superior) e externa ao IFRN Campus Pau dos Ferros.
        </p>
        <h2> <strong>Finalidades </strong></h2>
